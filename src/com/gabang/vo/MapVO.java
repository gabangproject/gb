package com.gabang.vo;

public class MapVO {	
	private String deposit;
	private String addr;
	private String x_position;
	private String y_position;
	private int num;
	private String parking_lot;
	private String monthly_rent;
	private String rtype;
	private String btype;

	public String getParking_lot() {
		return parking_lot;
	}

	public void setParking_lot(String parking_lot) {
		this.parking_lot = parking_lot;
	}

	public String getMonthly_rent() {
		return monthly_rent;
	}

	public void setMonthly_rent(String monthly_rent) {
		this.monthly_rent = monthly_rent;
	}

	public String getRtype() {
		return rtype;
	}

	public void setRtype(String rtype) {
		this.rtype = rtype;
	}

	public String getBtype() {
		return btype;
	}

	public void setBtype(String btype) {
		this.btype = btype;
	}

	public String getDeposit() {
		return deposit;
	}

	public void setDeposit(String deposit) {
		this.deposit = deposit;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public String getX_position() {
		return x_position;
	}

	public void setX_position(String x_position) {
		this.x_position = x_position;
	}

	public String getY_position() {
		return y_position;
	}

	public void setY_position(String y_position) {
		this.y_position = y_position;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
