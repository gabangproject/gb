package com.gabang.vo;

public class DealType {
	private String type;	// 거래형태
	private int num;		// 매물번호
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
}
